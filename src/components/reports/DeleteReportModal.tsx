import React, { useState } from 'react'
import { Report } from '../../services/reports'
import {
  HiExclamation,
  HiDocumentReport
} from 'react-icons/hi'
import ModalWrapper from '../../utils/ModalWrapper'

interface DeleteReportModalProps {
  report: Report
  isOpen: boolean
  onClose: () => void
  onDelete: (id: string) => Promise<void>
}

const DeleteReportModal: React.FC<DeleteReportModalProps> = ({
  report,
  isOpen,
  onClose,
  onDelete
}) => {
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)

  const handleDelete = async () => {
    try {
      setLoading(true)
      setError(null)
      await onDelete(report._id)
      onClose()
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Failed to delete report')
    } finally {
      setLoading(false)
    }
  }

  return (
    <ModalWrapper
      isOpen={isOpen}
      onClose={onClose}
      title="Delete Report"
      submitText="Delete Report"
      onSubmit={handleDelete}
      submitColor="bg-red-600"
    >
      <div className="p-4 space-y-4">
        {error && (
          <div className="bg-red-50 border border-red-200 text-red-700 px-4 py-3 rounded">
            {error}
          </div>
        )}

        <div className="flex items-center gap-3">
          <div className="p-2 bg-red-100 rounded-full">
            <HiExclamation className="w-6 h-6 text-red-600" />
          </div>
          <div>
            <h4 className="font-medium text-secondary-900">Are you sure?</h4>
            <p className="text-sm text-secondary-600">
              This action cannot be undone. This will permanently delete the report.
            </p>
          </div>
        </div>

        <div className="bg-secondary-50 p-4 rounded-lg">
          <div className="space-y-2">
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Title:</span>
              <span className="text-sm font-medium text-secondary-900">{report.title}</span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Type:</span>
              <span className="text-sm font-medium text-secondary-900 capitalize">{report.type}</span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Category:</span>
              <span className="text-sm font-medium text-secondary-900 capitalize">{report.category}</span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Status:</span>
              <span className="text-sm font-medium text-secondary-900 capitalize">{report.status}</span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Generated By:</span>
              <span className="text-sm font-medium text-secondary-900">{report.generatedBy}</span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Generated Date:</span>
              <span className="text-sm font-medium text-secondary-900">
                {report.generatedAt ? new Date(report.generatedAt).toLocaleDateString() : 'N/A'}
              </span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Format:</span>
              <span className="text-sm font-medium text-secondary-900 capitalize">{report.format}</span>
            </div>
            <div className="flex justify-between">
              <span className="text-sm text-secondary-600">Description:</span>
              <span className="text-sm font-medium text-secondary-900 max-w-xs truncate">
                {report.description || 'N/A'}
              </span>
            </div>
          </div>
        </div>

        <div className="bg-yellow-50 border border-yellow-200 text-yellow-800 px-4 py-3 rounded text-sm">
          <p className="font-medium mb-1">⚠️ Warning:</p>
          <p>Deleting this report will also affect:</p>
          <ul className="list-disc list-inside mt-1 space-y-1">
            <li>Report history and archives</li>
            <li>Data analysis records</li>
            <li>Compliance documentation</li>
            <li>Business intelligence data</li>
            <li>Audit trail information</li>
          </ul>
        </div>
      </div>
    </ModalWrapper>
  )
}

export default DeleteReportModal
